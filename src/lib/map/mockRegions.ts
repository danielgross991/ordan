import type { DeliveryRegion, LatLng, LngLat } from './supplierTypes'

function lngLatFromLatLng(points: LatLng[]): LngLat[] {
  return points.map(([lat, lng]) => [lng, lat])
}

function region(input: Omit<DeliveryRegion, 'geoPolygon'>): DeliveryRegion {
  return {
    ...input,
    geoPolygon: lngLatFromLatLng(input.polygon),
  }
}

